/**
 * LIOP HFT Oracle — HFT Dataset Generator
 *
 * Produces LIOP-compatible dataset snapshots from the live TickEngine state.
 * Used by the Oracle node to feed setSandboxData() with real-time HFT data.
 */
import type { TickEngine } from "./tick-engine.js";
import type { HftTickRecord } from "./types.js";

/**
 * Generates a snapshot of the current HFT engine state.
 * Compatible with LiopServer.setSandboxData().
 */
export function generateHftSnapshot(
	engine: TickEngine,
): HftTickRecord[] {
	return engine.getSnapshot();
}

